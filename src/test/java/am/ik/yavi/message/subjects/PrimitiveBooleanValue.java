package am.ik.yavi.message.subjects;

public class PrimitiveBooleanValue {

    private boolean value;

    public PrimitiveBooleanValue() {
    }

    public PrimitiveBooleanValue(boolean value) {
        this.value = value;
    }

    public boolean isValue() {
        return value;
    }

    public void setValue(boolean value) {
        this.value = value;
    }
}
