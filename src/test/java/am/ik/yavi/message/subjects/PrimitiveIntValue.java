package am.ik.yavi.message.subjects;

public class PrimitiveIntValue {

    private int value;

    public PrimitiveIntValue() {
    }

    public PrimitiveIntValue(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
